type Vars = Record<string, string | number>;

/** Look up a dot-path key in the locale dictionary, with {var} interpolation. */
export function translate(locale: Locale, key: string, vars?: Vars): string {
  const parts = key.split(".");
  let node: unknown = DICT[locale];
  for (const p of parts) {
    node = node && typeof node === "object" ? (node as Record<string, unknown>)[p] : undefined;
  }
  let str = typeof node === "string" ? node : key;
  if (vars) for (const [k, v] of Object.entries(vars)) str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
  return str;
}

/** Plural helper: picks _one/_other by count. */
export function plural(locale: Locale, base: string, n: number, vars?: Vars): string {
  const key = n === 1 ? `${base}_one` : `${base}_other`;
  return translate(locale, key, { n, ...vars });
}
